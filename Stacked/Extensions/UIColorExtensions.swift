import UIKit

extension UIColor {
    
    @nonobjc class var warmPink: UIColor {
        return UIColor(red: 1.0, green: 82.0 / 255.0, blue: 126.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var royalBlue: UIColor {
        return UIColor(red: 0.02, green: 0.11, blue: 0.29, alpha: 1.0)
    }
}
